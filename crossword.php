require('fpdf/fpdf.php');

class PDF_Crossword extends FPDF {

    var $col = 0;   
    var $y0;        

   
    function SetCol($col)
    {
        $this->col = $col;
        $x = 10 + $col * 65; 
        $this->SetLeftMargin($x);
        $this->SetX($x);
    }

    function AcceptPageBreak()
    {
        if ($this->col < 2) {
            $this->SetCol($this->col + 1);
            $this->SetY($this->y0);
            return false;
        } else {
            $this->SetCol(0);
            return true;
        }
    }
    
    function CrosswordGrid($grid, $cellSize = 5, $startX = 28, $startY = 16) {
        $this->SetFont('Arial','B',5);
        for ($i=0; $i<count($grid); $i++) {
            for ($j=0; $j<count($grid[$i]); $j++) {
                
                $cellValue = trim($grid[$i][$j]);

                $x = $startX + $j*$cellSize;
                $y = $startY + $i*$cellSize;
                if ($cellValue === '#') {
                    $this->SetFillColor(0,0,0);
                    $this->Rect($x,$y,$cellSize,$cellSize,'F');
                } else {
                    $this->Rect($x,$y,$cellSize,$cellSize);
                    if ($cellValue !== '') {
                        
                        if (is_numeric($cellValue)) {
                             $this->SetFont('Arial','',3.5); 
                             $this->Text($x + 0.5, $y + 2, $cellValue); 
                        } else if (ctype_alpha($cellValue)) {
                             $this->SetFont('Arial','B', 4);
                             $this->SetXY($x, $y); 
                             $this->Cell($cellSize, $cellSize, $cellValue, 0, 0, 'C');
                        } else {
                             $this->SetFont('Arial','B',5);
                             $this->Text($x+2, $y+3, $cellValue);
                        }
                    }
                }
            }
        }
         $this->SetFont('Arial','',8); 
    }
}

